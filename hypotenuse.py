x = float (input ("Введите катет 1: "))    #просим пользователя ввести катет1

y = float (input ("Введите катет 2: "))   #просим пользователя ввести катет2

q = x**2 + y**2    #вычисляем квадрат гипотенузы по суммам квадратов катетов

print ("Гипотенуза = ", q ** 0.5)     #вычисляем квадратный корень и выводим результат